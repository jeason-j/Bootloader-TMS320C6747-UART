//----------------------------------------------------------------------------------------------------
//   Function: DelayLoop
//      Input: Uint32 loopcnt: 循环次数
//     Output: void
//     Return: void
//Description: 等待循环
//    <AUTHOR>        <MODIFYTIME>            <REASON>
//   YanDengxue     2011-03-21 16:30           Create
//----------------------------------------------------------------------------------------------------
void DelayLoop(Uint32 loopcnt)
{
    Uint32 i;

    for (i = 0; i < loopcnt; i++)
    {
        asm("   NOP");
    }
}

